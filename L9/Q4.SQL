SET SERVEROUTPUT ON
DECLARE 
CURSOR C IS (SELECT * FROM STUDENT NATURAL JOIN TAKES WHERE COURSE_ID = 'CS-101') FOR UPDATE;
BEGIN
FOR BK IN C LOOP
DBMS_OUTPUT.PUT_LINE('ID:'||BK.ID||'    NAME:'||BK.NAME||'   COURSE_ID:'||BK.COURSE_ID);
IF BK.TOT_CREDITS<30 THEN
DELETE * FROM TAKES WHERE CURRENT OF C;
END IF;
END LOOP;
END;
/